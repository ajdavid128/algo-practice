function findShort(s){
    let arr = s.split(' ');
    let lengths = [];
    arr.map(e => lengths.push(e.length));
    lengths.sort((a,b) => a-b);
    return lengths[0];
  }

// Community Solution 1:
function findShort(s){
    return Math.min(...s.split(" ").map (s => s.length));
}

